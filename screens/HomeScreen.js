const ContactLisItem = (props) => {
  return (
    <View style={styles.container}>
      <Image source={{ uri: props.avatar ? 
        'http://lastminprod.com/Matcha/uploads/' + props.avatar : 
        'http://lastminprod.com/Matcha/uploads/avatar-placeholder.png' }} 
        style={styles.photo} />
      <Text style={styles.text}>
        {`${props.f_name} ${props.l_name}`}
      </Text>
    </View>
  )
}

class Messages extends React.Component {
  constructor(props) {
    super(props);
    const ds = new ListView.DataSource({
      rowHasChanged: (r1, r2) => r1 !== r2}
    );
    
    this.state = {
      search: '',
      start: 0,
      start_age: 9,
      end_age: 99,
      sort: 'age',
      number: 15,
      dataSource: null,
    };
    this._getData = this._getData.bind(this);
  }

  static navigationOptions = {
    title: 'Contacts',
    headerRight: (
      <HeaderButton
        side="right"
        title="New" />
    ),
  };

  async componentDidMount() {
    await this._getData();
    // await this.setState({loaded: true});
  }
  
  _getData = () => {
    let ds = new ListView.DataSource({
      rowHasChanged: (r1, r2) => r1 !== r2
    });
    fetch('http://lastminprod.com/Matcha/public/users', {
      method: 'POST',
      headers: {
        Accept: 'application/json',
        'Content-Type': 'application/json',
      },
      body: JSON.stringify(
        this.state
      )
    })
    .then((response) => response.json())
    .then((res) =>
    {
      let trueData = res.data;
      // console.warn(trueData);
      this.setState({dataSource: ds.cloneWithRows(trueData)});
      // console.warn(this.state.dataSource._dataBlob.s1);

    })
    .catch((error) => {
      console.error(error);
    });
  }

  render() {
    return (
      <View>
        <ScrollView>
          {this.state.dataSource && <ListView
            style={styles.container}
            dataSource={this.state.dataSource}
            renderRow={
              (data) => <ContactLisItem {...data} />
            }/>}
        </ScrollView>
      </View>
    );  
  }
}

const styles = StyleSheet.create({
  container: {
    flex: 1,
    padding: 12,
    flexDirection: 'row',
    backgroundColor: '#fff',
  },
  text: {
    marginLeft: 15,
    fontSize: 16,
  },
  photo: {
    top: -10,
    height: 40,
    width: 40,
    borderRadius: 20,
  },
  headerBtnRight: {
    right: 10,
    padding: 12,
    fontSize: 16,
    color: '#2386c8',
  },
});

export default Messages








